    loadLanguage : function(lang){
        var langs = ['en', 'de', 'es', 'fr', 'it', 'ja', 'nl', 'pl', 'ru'];
        //make sure we are loading Valence lang files
        // this is needed incase customer adds other languages we dont
        // support
        //
        if (langs.indexOf(lang) !== -1){
            var cls      = 'Valence.locale.' + lang,
                instance = Ext.create(cls);

            if (instance){
                Ext.apply(Valence.lang.lit,instance.lit);
            }
        }
    }
